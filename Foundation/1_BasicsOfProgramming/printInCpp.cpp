#include<iostream>
// "iostream" means Input Output Stream file includes code of "cout"
using namespace std;
// This is just to make sure scope is not limited

/* At first Main Function is called &
successful execution is indicated by 0 value returned */

int main(){

    // keyword - cout
    // << - Insertion Operator
    // Everything inside "Double Quotes" Prints as it is.
    // Just like fullstop in english we have semicolon ';' in programming
    cout<<"hello world\n";
    // "/n" is just a fancy way of saying "Press Enter Key"
    cout<<"bye"<<endl;
    // "endl" is alternate of "\n", means "End Line"
    cout<<"bye";

    return 0;
}
